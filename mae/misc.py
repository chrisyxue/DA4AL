def load_pretrained_model(args, model_without_ddp,strict=False,checkpoint_type='MAE'):
    if args.resume:
        if args.resume.startswith('https'):
            checkpoint = torch.hub.load_state_dict_from_url(
                args.resume, map_location='cpu', check_hash=True)
        else:
            checkpoint = torch.load(args.resume, map_location='cpu')

        try:
            if checkpoint_type == 'MAE':
                model_without_ddp.load_state_dict(checkpoint['model'])
            elif checkpoint_type == 'MoCo':
                params = checkpoint['state_dict']
                seleted_keys = [i for i in params.keys() if 'base_encoder' in i]
                seleted_params = {key.replace('module.base_encoder.',''):params[key] for key in seleted_keys}
                # pdb.set_trace()
                model_without_ddp.load_state_dict(seleted_params,strict=False)
            elif checkpoint_type == 'DINO':
                model_without_ddp.load_state_dict(checkpoint,strict=False)
                # pdb.set_trace()
            elif checkpoint_type == 'CXR':
                model_without_ddp.load_state_dict(checkpoint,strict=False)
            else:
                raise ValueError('No such checkpoint named '+checkpoint_type)
            # model_without_ddp.state_dict().keys()
        except RuntimeError:
            raise RecursionError('Params Not Match')
        print("Resume checkpoint %s" % args.resume)


def all_reduce_mean(x):
    world_size = get_world_size()
    if world_size > 1:
        x_reduce = torch.tensor(x).cuda()
        dist.all_reduce(x_reduce)
        x_reduce /= world_size
        return x_reduce.item()
    else:
        return x


