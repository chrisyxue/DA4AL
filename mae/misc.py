def accuracy(output, target, topk=(1,)):
    """Computes the accuracy over the k top predictions for the specified values of k"""
    with torch.no_grad():
        maxk = max(topk)
        batch_size = target.size(0)

        _, pred = output.topk(maxk, 1, True, True)
        pred = pred.t()
        correct = pred.eq(target.view(1, -1).expand_as(pred))

        res = []
        for k in topk:
            correct_k = correct[:k].contiguous().view(-1).float().sum(0, keepdim=True)
            res.append(correct_k.mul_(100.0 / batch_size))
        return res



def evlatuate_adapter(args,test_data_loader,model,model_adapt):
    device = args.device
    te_acc_lst = []
    with torch.no_grad():
        for i, samples in enumerate(test_data_loader):
            images, labels = samples
            images = images.to(device, non_blocking=True)
            labels = labels.to(device, non_blocking=True)
            feats,_,_ = model.forward_encoder(images,0)
            pre = model_adapt(feats)
            te_acc1 = accuracy(pre,labels,topk=(1,))
            te_acc_lst.append(te_acc1[0].item())
    te_acc = sum(te_acc_lst) / len(te_acc_lst)
    return te_acc